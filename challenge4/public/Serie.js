class Serie {
    name;
    creator;
    year;
    poster;
    watched;
    score;

    constructor(name, creator, year, poster, watched, score){
        this.name = name;
        this.creator = creator;
        this.year = year;
        this.poster = poster; 
        this.watched = watched;
        this.score = score;
    }
};

export default Serie;